  function getRandomInt(min, max) {
    return Math.floor(Math.random() * (max - min + 1)) + min;
}

function getRandomFloat(min, max) {
    return (Math.random() * (max - min) + min).toFixed(2);
}

function getRandomPreviousDate(startDate = new Date('2000-01-01')) {
    const endDate = new Date();
    const randomTime = startDate.getTime() + Math.random() * (endDate.getTime() - startDate.getTime());
    const randomDate = new Date(randomTime);
  
    const day = String(randomDate.getDate()).padStart(2, '0');
    const month = String(randomDate.getMonth() + 1).padStart(2, '0'); // Months are zero-based
    const year = randomDate.getFullYear();
  
    return `${day}-${month}-${year}`;
  }

function getRandomLabels() {
    const labels = [];
    const labelCount = getRandomInt(3, 6); // Random number of labels between 3 and 6
    const alphabet = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ';

    for (let i = 0; i < labelCount; i++) {
        const randomChar = alphabet.charAt(getRandomInt(0, alphabet.length - 1));
        labels.push(`Product ${randomChar}`);
    }

    return labels;
}

